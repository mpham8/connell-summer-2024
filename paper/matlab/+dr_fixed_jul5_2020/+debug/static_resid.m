function [lhs, rhs] = static_resid(y, x, params)
T = NaN(7, 1);
lhs = NaN(11, 1);
rhs = NaN(11, 1);
T(1) = y(3)^(-params(2));
T(2) = (1+params(4))^(-1);
T(3) = params(3)+1/params(5)*(1-T(1)/y(6));
T(4) = params(5)/2;
T(5) = 1-params(3)+T(4)*((y(5)/y(7))^2-params(3)^2);
T(6) = y(7)^(params(1)-1);
T(7) = T(4)*(y(5)/y(7)-params(3))^2;
lhs(1) = y(1);
rhs(1) = x(1)*y(7)^params(1);
lhs(2) = y(2);
rhs(2) = y(2)+y(2)*params(7)+y(3)+y(5)+params(6)-y(1)-x(3);
lhs(3) = T(1);
rhs(3) = T(1)*T(2)*(1+y(4));
lhs(4) = y(5);
rhs(4) = y(7)*T(3);
lhs(5) = y(6);
rhs(5) = T(2)*(y(6)*T(5)+T(6)*params(1)*T(1)*(1-x(2))*y(11));
lhs(6) = y(7);
rhs(6) = y(5)+y(7)*(1-params(3))-y(7)*T(7);
lhs(7) = y(8);
rhs(7) = y(6)*T(2)*(y(4)-params(7));
lhs(8) = y(9);
rhs(8) = params(8)-y(2);
lhs(9) = y(9);
rhs(9) = max(0,y(9)-y(8));
lhs(10) = y(10);
rhs(10) = y(4)-params(7);
lhs(11) = y(11);
rhs(11) = x(1);
end
